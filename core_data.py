# 14.2 ФУНКЦИЯ БИОСКАНИРОВАНИЯ (расширенная версия get_psychosomatic_profile)
def get_bio_scan_profile(results, tobolog_type=None):
    """
    Возвращает расширенный психосоматический профиль
    """
    risks = []
    element = results.get('protocol_element', '')
    organs_at_risk = ELEMENT_ORGANS.get(element, [])
    
    for organ_name in organs_at_risk:
        info = BIO_SCAN_MAP.get(organ_name, {})
        if info:
            risk = {
                'organ': organ_name,
                'system': info.get('system', ''),
                'emotion': info.get('emotion', ''),
                'thought': info.get('thought', ''),
                'body_sensation': info.get('body_sensation', ''),
                'illness': info.get('illness', ''),
                'practice': info.get('practice', ''),
                'affirmation': info.get('affirmation', ''),
                'aroma': info.get('aroma', ''),
                'color': info.get('color', ''),
                'block_type': info.get('block_type', '')
            }
            
            if tobolog_type is not None:
                if tobolog_type == 2:
                    risk['note'] = '⚠️ Вы игнорируете сигналы тела. Система может рухнуть внезапно.'
                elif tobolog_type == 3:
                    risk['note'] = '⚠️ Вы отрицаете проблему. Риск поздней диагностики.'
                elif tobolog_type == 4:
                    risk['note'] = '⚠️ Тревога усиливает симптомы. Практикуйте заземление.'
                elif tobolog_type == 5:
                    risk['note'] = '⚠️ Вы сканируете симптомы, создавая новые. Остановитесь.'
            
            risks.append(risk)
    
    return risks

