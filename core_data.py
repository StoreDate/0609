def diagnose_protocol_state(driver_element, protocol_element):
    """
    Диагностика состояния прошивки на основе пары стихий.
    Возвращает словарь с режимом, описанием и рекомендациями.
    """
    # Родные стихии (полное совпадение)
    if driver_element == protocol_element:
        return PROTOCOL_STATES['HARMONY'].copy()
    
    # Противоположные стихии (короткое замыкание)
    opposites = [('Огонь', 'Вода'), ('Вода', 'Огонь'), ('Воздух', 'Земля'), ('Земля', 'Воздух')]
    if (driver_element, protocol_element) in opposites:
        return PROTOCOL_STATES['SHORT_CIRCUIT'].copy()
    
    # Усиление (активный драйвер + активный протокол)
    active_elements = ['Огонь', 'Воздух']
    passive_elements = ['Вода', 'Земля']
    
    if driver_element in active_elements and protocol_element in active_elements:
        return PROTOCOL_STATES['ACCELERATION'].copy()
    
    # Торможение (пассивный драйвер + пассивный протокол)
    if driver_element in passive_elements and protocol_element in passive_elements:
        return PROTOCOL_STATES['DECELERATION'].copy()
    
    # Всё остальное — смешанный режим
    return PROTOCOL_STATES['MIXED_MODE'].copy()

