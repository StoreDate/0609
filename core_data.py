def reduce_to_single(value):
    if isinstance(value, int):
        s = str(value)
    elif isinstance(value, str):
        cleaned = ''.join(ch for ch in value if ch.isdigit())
        if not cleaned:
            return 0
        s = cleaned
    else:
        return 0
    while len(s) > 1 and s not in ('11', '13', '22', '33'):
        s = str(sum(int(d) for d in s))
    return int(s)

