# 14.4 ФУНКЦИЯ ОПРЕДЕЛЕНИЯ СТАТУСА ДЛЯ КОНТУРА
def get_contour_status(results, contour_key):
    user_element = results.get('protocol_element', 'Огонь')
    driver_element = results.get('driver_element', 'Огонь')
    
    # Используем состояние прошивки вместо сырого conflict
    protocol_state = results.get('protocol_state', {})
    has_conflict = protocol_state.get('name') == 'РЕЖИМ КОРОТКОГО ЗАМЫКАНИЯ'
    
    element_to_base = {'Огонь': 6, 'Вода': 5, 'Воздух': 4, 'Земля': 7}
    base = element_to_base.get(user_element, 5)
    
    contour_adjust = {
        'engine': 0,
        'memory': 1,
        'injection': -1,
        'fuel': 2
    }
    raw = base + contour_adjust.get(contour_key, 0)
    
    if has_conflict:
        raw -= 2
    
    if user_element == driver_element:
        raw += 1
    
    status = max(1, min(10, raw))
    return status

# 14.5 ФУНКЦИЯ ГЕНЕРАЦИИ ТЕКСТА ДЛЯ ОДНОГО КОНТУРА
def generate_contour_text(contour_key, status):
    """Возвращает словарь с текстом для одного контура"""
    
    contour = SITUATIONAL_CONTOURS.get(contour_key, {})
    status_info = STATUS_NAMES.get(status, ('НЕИЗВЕСТНО', 'unknown'))
    status_name = status_info[0]
    status_level = status_info[1]
    
    # Выбираем пул фраз
    if contour_key == 'engine':
        pool = ENGINE_SCENARIOS.get(status, ENGINE_SCENARIOS.get(5, []))
    elif contour_key == 'memory':
        pool = MEMORY_SCENARIOS.get(status, MEMORY_SCENARIOS.get(5, []))
    elif contour_key == 'injection':
        pool = INJECTION_SCENARIOS.get(status, INJECTION_SCENARIOS.get(5, []))
    elif contour_key == 'fuel':
        pool = FUEL_SCENARIOS.get(status, FUEL_SCENARIOS.get(5, []))
    else:
        pool = [('—', '—', '—', '—')]
    
    # Если пул пустой — берём дефолтный
    if not pool:
        pool = [('Ситуация не определена', 'Ресурс не оценён', 'Риск не вычислен', 'Практика: дыши')]
    
    # Берём случайный вариант
    scenario, resource, risk, practice = random.choice(pool)
    
    return {
        'icon': contour.get('icon', '🔧'),
        'name': contour.get('name', 'КОНТУР'),
        'element': contour.get('element', ''),
        'sphere': contour.get('sphere', ''),
        'status_number': status,
        'status_name': status_name,
        'status_level': status_level,
        'scenario': scenario,
        'resource': resource,
        'risk': risk,
        'practice': practice
    }

# 14.6 ФУНКЦИЯ ДЛЯ ВСЕХ 4 КОНТУРОВ
def get_all_situational_scenarios(results):
    """Возвращает словарь со сценариями для всех контуров"""
    if not results:
        return {}
    
    scenarios = {}
    for key in ['engine', 'memory', 'injection', 'fuel']:
        status = get_contour_status(results, key)
        scenarios[key] = generate_contour_text(key, status)
    
    return scenarios

# 14.7 ФУНКЦИЯ ДЛЯ ФОРМАТИРОВАННОГО ВЫВОДА В UI
def format_situational_scenarios(scenarios):
    """Возвращает отформатированный текст для вставки в UI"""
    if not scenarios:
        return "🎴 Данные для ситуативных сценариев отсутствуют\n"
    
    text = "\n🎛️ СИТУАТИВНЫЕ СЦЕНАРИИ (ДИАГНОСТИКА КОНТУРОВ)\n"
    text += "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n"
    
    order = ['engine', 'memory', 'injection', 'fuel']
    for key in order:
        s = scenarios.get(key, {})
        if not s:
            continue
        
        text += f"{s['icon']} {s['name']} | Статус: {s['status_number']} | {s['status_name']}\n"
        text += f"   Сценарий: {s['scenario']}\n"
        text += f"   Ресурс: {s['resource']}\n"
        text += f"   Риск: {s['risk']}\n"
        text += f"   Практика: {s['practice']}\n\n"
    
    text += "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n"
    return text

