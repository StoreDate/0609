# 13.6 ДИАГНОСТИКА БАЛАНСА СТИХИЙ В МАНДАЛЕ
MANDALA_ELEMENT_BALANCE = {
    'Огонь': ['1', '2', '3'],   # верхний ряд — воля, инициатива
    'Земля': ['4', '5', '6'],    # средний ряд — социум, тело
    'Вода': ['7', '8', '9'],     # нижний ряд — чувства, прошлое
    'Воздух': ['1', '4', '7'],   # левый столбец — прошлое, опыт
}

def get_mandala_element_distribution(counts):
    """Возвращает распределение стихий в мандале по всем 4 стихиям"""
    fire = sum(counts.get(int(d), 0) for d in MANDALA_ELEMENT_BALANCE['Огонь'] if d.isdigit())
    earth = sum(counts.get(int(d), 0) for d in MANDALA_ELEMENT_BALANCE['Земля'] if d.isdigit())
    water = sum(counts.get(int(d), 0) for d in MANDALA_ELEMENT_BALANCE['Вода'] if d.isdigit())
    air = sum(counts.get(int(d), 0) for d in MANDALA_ELEMENT_BALANCE['Воздух'] if d.isdigit())
    
    total = fire + earth + water + air
    if total == 0:
        return {}
    
    return {
        'Огонь': {'count': fire, 'percent': round(fire / total * 100, 1)},
        'Земля': {'count': earth, 'percent': round(earth / total * 100, 1)},
        'Вода': {'count': water, 'percent': round(water / total * 100, 1)},
        'Воздух': {'count': air, 'percent': round(air / total * 100, 1)},
        'total': total
    }

def get_mandala_element_advice(element_distribution):
    """Возвращает рекомендации по балансу стихий в мандале"""
    if not element_distribution:
        return []
    
    max_element = max(element_distribution.items(), key=lambda x: x[1]['percent'] if isinstance(x[1], dict) else 0)
    element_name, data = max_element
    
    if data['percent'] > 50:
        return [f"☯️ Перекос стихии {element_name}: {data['percent']}%. Добавьте противоположную стихию в жизнь."]
    
    weak_elements = [name for name, d in element_distribution.items() if d['percent'] < 15]
    if weak_elements:
        return [f"⚡ Слабая стихия: {', '.join(weak_elements)}. Практики по этой стихии усилят мандалу."]
    
    return ["✅ Гармоничное распределение стихий в мандале."]

