RANDOM_BASE_SEED = 42

def seeded_random(results):
    """Создаёт детерминированный seed на основе данных пользователя."""
    seed_str = f"{results.get('fate',0)}_{results.get('soul',0)}"
    seed_hash = int(hashlib.md5(seed_str.encode()).hexdigest()[:8], 16)
    random.seed(RANDOM_BASE_SEED + seed_hash)
    
