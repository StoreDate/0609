# 10. СТАРЫЙ КЛАСС OBDGenerator (сохранён без изменений)
#     Только добавлен новый метод generate_connected_report выше
# ============================================================

class OBDGenerator:
    """Генератор живого текста для всех блоков ОБОДа — все старые методы сохранены"""
    
    def __init__(self, results, mandala_data=None, tobolog_answers=None, vkb_answers=None, alexi_answers=None):
        self.r = results
        self.md = mandala_data
        self.tobolog = tobolog_answers
        self.vkb = vkb_answers
        self.alexi = alexi_answers
    
    # ── ECU ID ──
    def ecu_blob(self):
        if not self.r: return ""
        fate = self.r['fate']
        pool = ECU_POOLS.get(fate)
        if pool: 
            text = random.choice(pool)
            # Дополнительная пост-обработка на случай пропущенных плейсхолдеров
            text = text.replace('{flow}', _get_transition('flow') or 'Кстати')
            text = text.replace('{contrast}', _get_transition('contrast') or 'Но')
            text = text.replace('{emphasis}', _get_transition('emphasis') or 'Самое важное')
            text = text.replace('{result}', _get_transition('result') or 'В итоге')
            text = text.replace('{advice}', _get_transition('advice') or 'Вот что советую')
            return text
        fate_data = NUMBER_MEANINGS.get(fate, {})
        return fate_data.get('protocol', 'Система обрабатывает данные.')
    
    # ── Активационный ключ ──
    def activation_blob(self):
        if not self.r: return ""
        soul = self.r['soul']
        pool = ACTIVATION_POOLS.get(soul)
        if pool:
            text = random.choice(pool)
            text = text.replace('{flow}', _get_transition('flow') or 'Кстати')
            text = text.replace('{contrast}', _get_transition('contrast') or 'Но')
            text = text.replace('{emphasis}', _get_transition('emphasis') or 'Самое важное')
            text = text.replace('{result}', _get_transition('result') or 'В итоге')
            text = text.replace('{advice}', _get_transition('advice') or 'Вот что советую')
            text = text.replace('{elaboration}', _get_transition('elaboration') or 'Иными словами')
            return text
        soul_data = NUMBER_MEANINGS.get(soul, {})
        return soul_data.get('protocol', 'Ключ активации принят.')
    
    # ── Связка ECU + Ключ ──
    def ecu_key_connection(self):
        if not self.r: return ""
        driver_el = self.r.get('driver_element', '')
        protocol_el = self.r.get('protocol_element', '')
        if driver_el == protocol_el:
            pool = ECU_KEY_CONNECTIONS['same_element']
        elif set([driver_el, protocol_el]) in [{'Огонь', 'Вода'}, {'Вода', 'Огонь'}, {'Воздух', 'Земля'}, {'Земля', 'Воздух'}]:
            pool = ECU_KEY_CONNECTIONS['opposite_elements']
        else:
            pool = ECU_KEY_CONNECTIONS['compatible_elements']
        text = random.choice(pool)
        text = text.replace('{harmony}', random.choice(SMOOTH_TRANSITIONS['metaphor_bridges']['harmony']))
        text = text.replace('{tension}', random.choice(SMOOTH_TRANSITIONS['metaphor_bridges']['tension']))
        text = text.replace('{flow}', _get_transition('flow') or 'Кстати')
        text = text.replace('{contrast}', _get_transition('contrast') or 'Но')
        text = text.replace('{result}', _get_transition('result') or 'В итоге')
        text = text.replace('{advice}', _get_transition('advice') or 'Вот что важно')
        return text
    
    # ── Драйвер ──
    def driver_blob(self):
        driver = self.r.get('driver_human', {}) if self.r else {}
        if not driver: return ""
        templates = [
            f"Твой драйвер — {driver.get('name', '')}. {driver.get('short', '')}. {driver.get('description', '')}",
            f"Под капотом — {driver.get('name', '')}. {driver.get('description', '')} В быту это значит: {driver.get('strength', '').lower()}",
        ]
        return random.choice(templates)
    
    # ── Диагностика прошивки ──
    def diagnosis_blob(self):
        if not self.r: return ""
        state = self.r.get('protocol_state', {})
        if not state: return ""
        diagnosis_text = state.get('description', '')
        feel_text = state.get('feel_like', '')
        practice_text = state.get('practice', '')
        state_name = state.get('name', '')
        if 'ЗАВОДСКАЯ' in state_name: example_key = 'HARMONY'
        elif 'ПЕРЕГРУЗКИ' in state_name: example_key = 'ACCELERATION'
        elif 'ТОРМОЖЕНИЯ' in state_name: example_key = 'DECELERATION'
        elif 'КОРОТКОЕ' in state_name: example_key = 'SHORT_CIRCUIT'
        else: example_key = 'MIXED_MODE'
        example = random.choice(PROTOCOL_EXAMPLES.get(example_key, [""]))
        # Пост-обработка примера на случай плейсхолдеров
        example = example.replace('{harmony}', random.choice(SMOOTH_TRANSITIONS['metaphor_bridges']['harmony']))
        example = example.replace('{stress}', random.choice(SMOOTH_TRANSITIONS['metaphor_bridges']['stress']))
        example = example.replace('{tension}', random.choice(SMOOTH_TRANSITIONS['metaphor_bridges']['tension']))
        example = example.replace('{flow}', _get_transition('flow') or 'Кстати')
        example = example.replace('{contrast}', _get_transition('contrast') or 'Но')
        example = example.replace('{advice}', _get_transition('advice') or 'Вот что я предлагаю')
        example = example.replace('{emphasis}', _get_transition('emphasis') or 'Запомни')
        return f"{diagnosis_text}\n\n{example}\n\n🔧 {practice_text}"
    
    # ── Ситуативные сценарии ──
    def scenario_blob(self, contour_key, status_level):
        pool = SITUATIONAL_ADVICE.get(contour_key, {}).get(status_level, ["Система в норме."])
        text = random.choice(pool)
        text = text.replace('{advice}', _get_transition('advice') or 'Советую')
        text = text.replace('{contrast}', _get_transition('contrast') or 'Но')
        text = text.replace('{elaboration}', _get_transition('elaboration') or 'По сути')
        text = text.replace('{result}', _get_transition('result') or 'В итоге')
        text = text.replace('{flow}', _get_transition('flow') or 'Кстати')
        return text
    
    # ── Совместимость ──
    def compatibility_blob(self, comp_result):
        level = comp_result.get('level', 'neutral')
        message = comp_result.get('message', '')
        details = comp_result.get('details', '')
        metaphor = random.choice(COMPATIBILITY_METAPHORS.get(level, ["Данные обработаны."]))
        metaphor = metaphor.replace('{result}', _get_transition('result') or 'В итоге')
        metaphor = metaphor.replace('{contrast}', _get_transition('contrast') or 'Но')
        metaphor = metaphor.replace('{advice}', _get_transition('advice') or 'Совет')
        metaphor = metaphor.replace('{flow}', _get_transition('flow') or 'Кстати')
        blob = f"{message}\n\n{metaphor}"
        if details: blob += f"\n\n📖 {details}"
        return blob
    
    # ── Мандала ──
    def mandala_rich_blob(self):
        if not self.r or not self.md or not self.md.get('rich'): 
            return "Густых цифр нет. Энергия распределена равномерно."
        blobs = []
        for d in self.md['rich'][:5]:
            pool = MANDALA_RICH_IMAGES.get(d, [f"Цифра {d} встречается часто. Это твоя зона силы."])
            text = random.choice(pool)
            text = text.replace('{flow}', _get_transition('flow') or 'Кстати')
            text = text.replace('{result}', _get_transition('result') or 'В итоге')
            text = text.replace('{advice}', _get_transition('advice') or 'Совет')
            blobs.append(text)
        return "\n".join([f"• {b}" for b in blobs])
    
    def mandala_empty_blob(self):
        if not self.r or not self.md or not self.md.get('empty'): 
            return "Пустых цифр нет. Все каналы активны."
        blobs = []
        for d in self.md['empty'][:5]:
            pool = MANDALA_EMPTY_IMAGES.get(d, [f"Цифра {d} отсутствует. Это зона роста."])
            text = random.choice(pool)
            text = text.replace('{flow}', _get_transition('flow') or 'Кстати')
            text = text.replace('{result}', _get_transition('result') or 'В итоге')
            text = text.replace('{advice}', _get_transition('advice') or 'Совет')
            text = text.replace('{contrast}', _get_transition('contrast') or 'Но')
            text = text.replace('{elaboration}', _get_transition('elaboration') or 'По сути')
            blobs.append(text)
        return "\n".join([f"• {b}" for b in blobs])
    
    # ── Психосоматика ──
    def psycho_driver_link(self):
        if not self.r: return ""
        driver_el = self.r.get('driver_element', '')
