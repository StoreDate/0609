def _get_transition(key: str, **kwargs) -> str:
    """Возвращает случайный переход с подстановкой параметров"""
    if key in SMOOTH_TRANSITIONS:
        pool = SMOOTH_TRANSITIONS[key]
        if isinstance(pool, list):
            template = random.choice(pool)
            return template.format(**kwargs) if kwargs else template
    # fallback для flow_phrases
    if key in SMOOTH_TRANSITIONS.get('flow_phrases', {}):
        pool = SMOOTH_TRANSITIONS['flow_phrases'][key]
        return random.choice(pool)
    return ""


def _smooth_join(segment1: str, segment2: str, transition_key: str = None, 
                 extra_context: dict = None) -> str:
    """
    Плавно соединяет два текстовых сегмента.
    Если сегменты уже как-то связаны — просто склеивает.
    Иначе добавляет подходящий переход.
    """
    if not segment1 or not segment2:
        return segment1 + segment2 if segment2 else segment1
    
    # Проверяем, нет ли уже связки в конце первого сегмента
    last_sentence = segment1.rstrip('.').split('.')[-1] if '.' in segment1 else segment1
    if any(word in last_sentence.lower() for word in ['но', 'однако', 'и всё же', 'при этом', 'кстати']):
        # Уже есть переход — просто добавляем пробел
        return segment1 + "\n\n" + segment2
    
    # Добавляем переход
    if transition_key:
        bridge = _get_transition(transition_key, **(extra_context or {}))
        if bridge:
            # Делаем первую букву второго сегмента строчной, если это уместно
            second_start = segment2[0].lower() + segment2[1:] if segment2 and segment2[0].isupper() else segment2
            return segment1 + "\n\n" + bridge + " " + second_start
    
    # Если перехода нет или он не подошёл — используем контрастную связку
    contrast = _get_transition('contrast')
    if contrast:
        second_start = segment2[0].lower() + segment2[1:] if segment2 and segment2[0].isupper() else segment2
        return segment1 + "\n\n" + contrast + ", " + second_start
    
    # Самый простой fallback
    return segment1 + "\n\n" + segment2


