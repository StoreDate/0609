# 9. НОВЫЙ МЕТОД — ГЕНЕРАЦИЯ СВЯЗНОГО ОТЧЁТА
# ============================================================

def generate_connected_report(
    results,
    mandala_data=None,
    tobolog_answers=None,
    vkb_answers=None,
    alexi_answers=None,
    compatibility=None,
    scenarios=None
) -> str:
    """
    НОВЫЙ МЕТОД: генерирует связный, плавный отчёт,
    где все блоки соединены переходами и образуют единое повествование.
    
    Старые методы (ecu_blob, activation_blob и т.д.) остаются без изменений
    для обратной совместимости с UI.
    """
    
    if not results:
        return "Нет данных для диагностики."
    
    gen = OBDGenerator(results, mandala_data, tobolog_answers, vkb_answers, alexi_answers)
    
    story_parts = []
    
    # 1. Открытие
    story_parts.append(_get_transition('openings'))
    
    # 2. ECU
    ecu_text = gen.ecu_blob()
    story_parts.append(ecu_text)
    
    # 3. Ключ (с переходом)
    if results.get('soul'):
        key_text = gen.activation_blob()
        if key_text:
            story_parts.append(_smooth_join(ecu_text, key_text, 'ecu_to_key'))
            current_text = key_text
        else:
            current_text = ecu_text
    else:
        current_text = ecu_text
    
    # 4. Связка ECU + ключ
    conn_text = gen.ecu_key_connection()
    if conn_text:
        story_parts.append(_smooth_join(current_text, conn_text, 'key_to_connection'))
        current_text = conn_text
    
    # 5. Драйвер
    driver_text = gen.driver_blob()
    if driver_text:
        story_parts.append(_smooth_join(current_text, driver_text, 'connection_to_driver'))
        current_text = driver_text
    
    # 6. Состояние прошивки
    diag_text = gen.diagnosis_blob()
    if diag_text:
        story_parts.append(_smooth_join(current_text, diag_text, 'driver_to_firmware'))
        current_text = diag_text
    
    # 7. Переход к контурам (если есть сценарии)
    if scenarios:
        story_parts.append(_get_transition('to_contours'))
        contours_text = ""
        for key in ['engine', 'memory', 'fuel', 'injection']:
            if key in scenarios:
                s = scenarios[key]
                level = 'normal'
                if s.get('status_number', 5) <= 3:
                    level = 'low'
                elif s.get('status_number', 5) >= 7:
                    level = 'high'
                contours_text += f"▸ {s.get('name', key)}: {gen.scenario_blob(key, level)}\n"
        if contours_text:
            story_parts.append(contours_text)
            current_text = contours_text
    
    # 8. Совместимость
    if compatibility:
        story_parts.append(_get_transition('to_compatibility'))
        compat_text = gen.compatibility_blob(compatibility)
        story_parts.append(compat_text)
        current_text = compat_text
    
    # 9. Мандала
    if mandala_data:
        story_parts.append(_get_transition('to_mandala'))
        mandala_lines = []
        if mandala_data.get('rich'):
            rich_text = gen.mandala_rich_blob()
            if rich_text and rich_text != "Густых цифр нет. Энергия распределена равномерно.":
                mandala_lines.append(rich_text)
        if mandala_data.get('empty'):
            empty_text = gen.mandala_empty_blob()
            if empty_text and empty_text != "Пустых цифр нет. Все каналы активны.":
                mandala_lines.append(empty_text)
        if mandala_lines:
            story_parts.append("\n".join(mandala_lines))
    
    # 10. Тесты
    tests_parts = []
    if tobolog_answers:
        tobolog_type = get_tobolog_type(tobolog_answers) if callable(get_tobolog_type) else 1
        tests_parts.append(gen.test_tobolog_result(tobolog_type))
    if vkb_answers:
        vkb_level = vkb_answers.get('level', 1) if isinstance(vkb_answers, dict) else 1
        tests_parts.append(gen.test_vkb_result(vkb_level))
    if alexi_answers:
        tests_parts.append(gen.test_alexi_result())
    if tests_parts:
        story_parts.append(_get_transition('to_tests'))
        story_parts.append("\n".join(tests_parts))
    
    # 11. Психосоматика
    psycho_text = gen.psycho_driver_link()
    if psycho_text:
        story_parts.append(_get_transition('to_psycho'))
        story_parts.append(psycho_text)
    
    # 12. Закрытие
    story_parts.append(_get_transition('closings'))
    
    # Склеиваем всё с двойным переводом строки
    full_story = "\n\n".join(p for p in story_parts if p and p.strip())
    
    return full_story

